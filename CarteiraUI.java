    private void BotaoFecharCarteiraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoFecharCarteiraActionPerformed
        // TODO add your handling code here:

        String password = JOptionPane.showInputDialog("Obs: Após fechar a carteira não será mais possivel logar no sistema \nDigite a password:");

        RepositorioDasCarteiras repositorioDasCarteiras = RepositorioDasCarteiras.pegarInstancia();

        Carteira carteira = repositorioDasCarteiras.consultarCarteira(email);

        if (password.equals(carteira.getPassword())) {
            repositorioDasCarteiras.fecharCarteira(email);
        }

        dispose();

    }//GEN-LAST:event_BotaoFecharCarteiraActionPerformed

    private void BotaoTransferirSaldoEmMoedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoTransferirSaldoEmMoedaActionPerformed
        // TODO add your handling code here:
        String emailReceptor = JOptionPane.showInputDialog("Digite o email a receber a tranferência:");

        Double valor = Double.valueOf(JOptionPane.showInputDialog("Digite o valor em moeda a tranferir:"));

        RepositorioDasCarteiras repositorioDasCarteiras = RepositorioDasCarteiras.pegarInstancia();

        repositorioDasCarteiras.realizarTransferencia(email, valor, emailReceptor);

        ActualizarRotulos(email);

    }//GEN-LAST:event_BotaoTransferirSaldoEmMoedaActionPerformed


