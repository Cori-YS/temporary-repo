    private void ActualizarRotulos(String email) {
        RepositorioDasCarteiras repositorioDasCarteiras = RepositorioDasCarteiras.pegarInstancia();

        Carteira carteira = repositorioDasCarteiras.consultarCarteira(email);

        this.EmailLabel.setText(carteira.getEmail());
        this.NomeCompletoLabel.setText(carteira.getNomeCompleto());
        this.SaldoLabel.setText(String.valueOf(carteira.getSaldoUnitel()));
        this.SaldoEmMoedaLabel.setText(String.valueOf(carteira.getSaldoEmMoeda()));
    }

